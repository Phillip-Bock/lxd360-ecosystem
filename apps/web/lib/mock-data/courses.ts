const createModules = (courseId: string, moduleCount: number): Module[] => {
  const inspireStages: INSPIREStage[] = [
    'ignite',
    'navigate',
    'scaffold',
    'practice',
    'integrate',
    'reflect',
    'extend',
  ];

  return Array.from({ length: moduleCount }, (_, i) => ({
    id: `${courseId}-module-${i + 1}`,
    courseId,
    title: `Module ${i + 1}: ${['Introduction', 'Core Concepts', 'Advanced Topics', 'Practical Application', 'Assessment'][i] || `Part ${i + 1}`}`,
    description: `Module ${i + 1} of this course covering key topics and concepts.`,
    order: i + 1,
    duration: Math.floor(Math.random() * 30) + 15,
    lessons: Array.from({ length: Math.floor(Math.random() * 4) + 3 }, (_, j) => ({
      id: `${courseId}-module-${i + 1}-lesson-${j + 1}`,
      moduleId: `${courseId}-module-${i + 1}`,
      title: `Lesson ${j + 1}`,
      description: `Lesson ${j + 1} content description`,
      order: j + 1,
      duration: Math.floor(Math.random() * 15) + 5,
      contentType: (['video', 'text', 'interactive', 'document'] as const)[
        Math.floor(Math.random() * 4)
      ],
      inspireStage: inspireStages[Math.min(i, inspireStages.length - 1)],
      cognitiveLoadTarget: (['low', 'medium', 'high'] as const)[Math.floor(Math.random() * 3)],
      isPreviewable: j === 0,
      isRequired: true,
      xpReward: 50,
      resources: [],
      hasTranscript: true,
      hasCaptions: true,
      hasAudioDescription: false,
    })),
    isLocked: i > 0,
    completionRequirement: 'all',
  }));
};

