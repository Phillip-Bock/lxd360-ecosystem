// Helper functions
export function getCourseById(id: string): Course | undefined {
  return mockCourses.find((course) => course.id === id);
}

export function getCoursesByCategory(categoryId: string): Course[] {
  return mockCourses.filter((course) => course.category.id === categoryId);
}

export function getRequiredCourses(): Course[] {
  return mockCourses.filter((course) => course.isRequired);
}

export function getPopularCourses(limit: number = 10): Course[] {
  return [...mockCourses].sort((a, b) => b.enrollmentCount - a.enrollmentCount).slice(0, limit);
}

export function getNewCourses(limit: number = 10): Course[] {
  return [...mockCourses]
    .sort(
      (a, b) =>
        new Date(b.publishedAt || b.createdAt).getTime() -
        new Date(a.publishedAt || a.createdAt).getTime(),
    )
    .slice(0, limit);
}

export function searchCourses(query: string): Course[] {
  const searchTerm = query.toLowerCase();
  return mockCourses.filter(
    (course) =>
      course.title.toLowerCase().includes(searchTerm) ||
      course.description.toLowerCase().includes(searchTerm) ||
      course.tags.some((tag) => tag.toLowerCase().includes(searchTerm)),
  );
}
